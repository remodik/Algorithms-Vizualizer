function renderDesc(containerId, algoKey) {
  const el = document.getElementById(containerId);
  if (!el) return;
  const d = DESCRIPTIONS[algoKey];
  if (!d) {
    el.innerHTML = "";
    return;
  }

  const stableLabel =
    d.stable === null
      ? ""
      : d.stable
        ? '<span class="desc-badge desc-stable">стабильный</span>'
        : '<span class="desc-badge desc-unstable">нестабильный</span>';

  const complexityRows = d.complexity
    .map(
      (c) => `
    <tr>
      <td class="desc-tc-case">${c.case}</td>
      <td class="desc-tc-time">${c.time}</td>
      <td class="desc-tc-space">${c.space}</td>
      <td class="desc-tc-note">${c.note}</td>
    </tr>`,
    )
    .join("");

  const pros = d.pros.map((p) => `<li class="desc-pro">✓ ${p}</li>`).join("");
  const cons = d.cons.map((c) => `<li class="desc-con">✗ ${c}</li>`).join("");

  el.innerHTML = `
<div class="desc-block">
  <div class="desc-header">
    <span class="desc-title">${d.title}</span>
    ${stableLabel}
  </div>

  <div class="desc-section">
    <div class="desc-label">Идея</div>
    <div class="desc-idea">${d.idea.replace(/\n/g, "<br>")}</div>
  </div>

  <div class="desc-section">
    <div class="desc-label">Сложность</div>
    <table class="desc-table">
      <thead><tr>
        <th>Случай</th><th>Время</th><th>Память</th><th>Примечание</th>
      </tr></thead>
      <tbody>${complexityRows}</tbody>
    </table>
  </div>

  <div class="desc-pros-cons">
    <div class="desc-section">
      <div class="desc-label">Плюсы</div>
      <ul class="desc-list">${pros}</ul>
    </div>
    <div class="desc-section">
      <div class="desc-label">Минусы</div>
      <ul class="desc-list">${cons}</ul>
    </div>
  </div>

  <div class="desc-section">
    <div class="desc-label">Когда использовать</div>
    <div class="desc-when">${d.when}</div>
  </div>
</div>`;
}
